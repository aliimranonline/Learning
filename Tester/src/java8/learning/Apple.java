package java8.learning;

public class Apple {
	
	public Integer weight;
	public String color;
	
	public Apple() {
		
	}
	
	
	
	public Apple(Integer weight, String color) {
		super();
		this.weight = weight;
		this.color = color;
	}



	public Integer getWeight() {
		return weight;
	}
	public void setWeight(Integer weight) {
		this.weight = weight;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	

}
